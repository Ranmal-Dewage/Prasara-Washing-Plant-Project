            con.prepareStatement("UPDATE processplan set W_Supervisor = '"+wsu1+"', Employee = '"+emp1+"', BC_QC = '"+bcqc1+"', BW_Store = '"+bwst1+"' WHERE Process_No = "+pno+"").execute();
