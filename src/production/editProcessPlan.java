        String emp1 = String.valueOf(PPEmp.getSelectedItem());
        String bwst1 = String.valueOf(PPBWSt.getSelectedItem());
        String wsu1 = String.valueOf(PPWSu.getSelectedItem());
        String bcqc1 = String.valueOf(PPBC.getSelectedItem());
        
        int pno = Integer.parseInt(p);
        
