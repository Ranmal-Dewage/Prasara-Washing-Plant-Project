    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        customer.setBorder(BorderFactory.createLineBorder(Color.decode("#ffffff")));
        customerl.setText("");
        style.setBorder(BorderFactory.createLineBorder(Color.decode("#ffffff")));
        stylel.setText("");
        colNo.setBorder(BorderFactory.createLineBorder(Color.decode("#a0a0a0")));
        colNol.setText("");
        colName.setBorder(BorderFactory.createLineBorder(Color.decode("#a0a0a0")));
        colNamel.setText("");

        String col = colNo.getText();
        String cName = colName.getText();
        String cus = String.valueOf(customer.getSelectedItem());
        String styl = String.valueOf(style.getSelectedItem());

        try {
            if ((formValidate()) == true) {
                if (st.equals("edit")) {
                    con.prepareStatement("UPDATE color set colorName='" + cName + "' WHERE customer ='" + pk + "' AND style = '" + pk2 + "' AND color ='" + editCol + "'").execute();
                    JOptionPane.showMessageDialog(null, "Color Updated", "Done", JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                } else {
                    try {
                        con.prepareStatement("INSERT INTO color VALUES('" + cus + "','" + styl + "','" + col + "','" + cName + "')").execute();
                        JOptionPane.showMessageDialog(null, "New Color Added", "Done", JOptionPane.INFORMATION_MESSAGE);
                        colNo.setText(null);
                        colName.setText(null);
                        customer.setSelectedIndex(-1);
                        style.setSelectedIndex(-1);
                    } catch (SQLException ex) {
                        Logger.getLogger(addColor.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(addColor.class.getName()).log(Level.SEVERE, null, ex);
        }
        Home.viewColor.refresh();
    }//GEN-LAST:event_jButton1ActionPerformed

