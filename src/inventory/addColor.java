    public addColor(String s, String p, String p2, String c) throws SQLException {
        initComponents();
        st = s;
        pk = p;
        pk2 = p2;
        editCol = c;
        dbCon d = new dbCon();
        d.connect();

        ResultSet rs = con.createStatement().executeQuery("SELECT DISTINCT customer FROM style");
        while (rs.next()) {
            customer.addItem(rs.getString("customer"));
        }
        ResultSet rs2 = con.createStatement().executeQuery("SELECT styleNo FROM style WHERE customer ='" + pk + "'");
        while (rs2.next()) {
            style.addItem(rs2.getString("styleNo"));
        }

        ResultSet edit = con.createStatement().executeQuery("SELECT * FROM color WHERE customer = '" + pk + "' AND style = '" + pk2 + "' AND color ='" + editCol + "'");
        edit.next();
        colNo.setText(edit.getString("color"));
        colName.setText(edit.getString("colorName"));

        colNo.setEditable(false);
        jLabel1.setText("Edit Style Color");
        jButton1.setText("Edit Color");

        customer.setSelectedItem(pk);
        style.setSelectedItem(pk2);

        customer.setEnabled(false);
        style.setEnabled(false);
        colNo.setEditable(false);
    }

    boolean formValidate() throws SQLException {
        if ((customer.getSelectedIndex()) != -1) {
            if ((style.getSelectedIndex()) != -1) {
                if (colNo.getText().equals("")) {
                    colNo.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
                    colNol.setText("please add a color code");
                    return false;
                } else {
                    if (colName.getText().equals("")) {
                        colName.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
                        colNamel.setText("please add a color name");
                        return false;
                    } else {
                        ResultSet rs = con.createStatement().executeQuery("SELECT color FROM color WHERE customer = '" + customer.getSelectedItem() + "' AND style ='" + style.getSelectedItem() + "' AND color ='" + colNo.getText() + "'");
                        if (rs.next()) {
                            if (st.equals("edit")) {
                                return true;
                            } else {
                                colNo.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
                                colNol.setText("customer's style color exist");
                                return false;
                            }
                        } else {
                            return true;
                        }
                    }
                }
            } else {
                style.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
                stylel.setText("please select a style");
                return false;
            }
        } else {
            customer.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
            customerl.setText("please select a customer");
            return false;
        }
    }

