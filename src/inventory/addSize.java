    boolean formValidate() throws SQLException {
        if (customer.getSelectedIndex() != -1) {
            if (style.getSelectedIndex() != -1) {
                if (size.getText().equals("")) {
                    sizel.setText("please add a size");
                    size.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
                    return false;
                } else {
                    ResultSet rs = con.createStatement().executeQuery("SELECT size FROM size WHERE customer = '" + customer.getSelectedItem() + "' AND style ='" + style.getSelectedItem() + "' AND size = '" + size.getText() + "'");
                    if (rs.next()) {
                        sizel.setText("customer's style size exist");
                        size.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
                        return false;
                    } else {
                        return true;
                    }
                }
            } else {
                stylel.setText("please select a style");
                style.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
                return false;
            }
        } else {
            customerl.setText("please select a customer");
            customer.setBorder(BorderFactory.createLineBorder(Color.decode("#ff0000")));
            //JOptionPane.showMessageDialog(null, "select customer", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

