    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            style.setBorder(BorderFactory.createLineBorder(Color.decode("#ffffff")));
            customer.setBorder(BorderFactory.createLineBorder(Color.decode("#ffffff")));
            size.setBorder(BorderFactory.createLineBorder(Color.decode("#a0a0a0")));
            stylel.setText("");
            customerl.setText("");
            sizel.setText("");

            String siz = size.getText();
            String cus = String.valueOf(customer.getSelectedItem());
            String styl = String.valueOf(style.getSelectedItem());

            if ((formValidate()) == true) {
                try {
                    con.prepareStatement("INSERT INTO size VALUES('" + cus + "','" + styl + "','" + siz + "')").execute();
                    JOptionPane.showMessageDialog(null, "New Size Added", "Done", JOptionPane.INFORMATION_MESSAGE);
                    size.setText(null);
                    customer.setSelectedIndex(-1);
                    style.setSelectedIndex(-1);
                } catch (SQLException ex) {
                    Logger.getLogger(addSize.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(addSize.class.getName()).log(Level.SEVERE, null, ex);
        }
        Home.vSize.refresh();
    }//GEN-LAST:event_jButton1ActionPerformed

