    void updateMax(String q) throws SQLException {
        ResultSet rs = con.createStatement().executeQuery(q);
        rs.next();
        tot = rs.getInt("count");

        int i = tot % lim;
        if (i > 0) {
            maxPages = tot / lim + 1;
        } else {
            maxPages = tot / lim;
        }
        total.setText(Integer.toString(maxPages));

        if (pg > maxPages) {
            pg = maxPages;
            page.setText(Integer.toString(pg));
            if (pg == 0) {
                offset = (pg) * lim;
            } else {
                offset = (pg - 1) * lim;
            }
        }
        if (maxPages == 1) {
            pg = 1;
            page.setText(Integer.toString(pg));
            if (pg == 0) {
                offset = (pg) * lim;
            } else {
                offset = (pg - 1) * lim;
            }
        }
    }

    void addTable(String query) {
        try {
            ResultSet rs = con.createStatement().executeQuery(query);

            DefaultTableModel model = new DefaultTableModel(new Object[]{"Style", "Garment Item"}, 0);
            while (rs.next()) {
                String gi = rs.getString("garmentItem");
                String stl = rs.getString("style");

                Object[] row = {stl, gi};
                model.addRow(row);
            }
            jTable1.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(viewColor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void search() throws SQLException {
        String sh = search.getText();
        if (sh.equals("")) {
            updateMax("SELECT COUNT(*) AS count FROM garments");
            addTable("SELECT * FROM garments LIMIT " + lim + " OFFSET " + offset + "");
        } else {
            String q = "SELECT * FROM garments WHERE style like '%" + sh + "%' or garmentItem like '%" + sh + "%'";
            addTable(q);
        }
    }

