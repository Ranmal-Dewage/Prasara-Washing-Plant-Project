    void updateMax(String q, javax.swing.JLabel tox, javax.swing.JTextField pad) {
        try {
            ResultSet rs = dbCon.con.createStatement().executeQuery(q);
            rs.next();
            tot = rs.getInt("count");

            int i = tot % lim;
            if (i > 0) {
                maxPages = tot / lim + 1;
            } else {
                maxPages = tot / lim;
            }
            tox.setText(Integer.toString(maxPages));

            if (pg > maxPages) {
                pg = maxPages;
                pad.setText(Integer.toString(pg));
                if (pg == 0) {
                    offset = (pg) * lim;
                } else {
                    offset = (pg - 1) * lim;
                }
            }
            if (maxPages == 1) {
                pg = 1;
                pad.setText(Integer.toString(pg));
                if (pg == 0) {
                    offset = (pg) * lim;
                } else {
                    offset = (pg - 1) * lim;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(POGarmentView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void addToTable(String query) {

        try {
            ResultSet rs = dbCon.con.createStatement().executeQuery(query);

            DefaultTableModel model = (DefaultTableModel) GarmentPO.getModel();

            model.getDataVector().removeAllElements();

            while (rs.next()) {
                String bn = rs.getString("p.portionNo");
                String pd = rs.getString("poDate");
                String ed = rs.getString("expDate");
                String cus = rs.getString("customer");
                String po = rs.getString("poNo");
                String tlm = rs.getString("totQty");
                String tc = rs.getString("totCost");

                Object[] rowData = {pd, ed, po, cus, bn, tlm, tc};
                model.addRow(rowData);
            }

            GarmentPO.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(POGarmentView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

